//Syntax Explanation :
//	Describe what the following line of C++ code does : int* ptr = &var; .
//
//	Създава пойнтер "ptr", който сочи към адреса на паметта, където се съхранява променливата "var". 
//	Това ни позволява да манипулиране на стойността на "var" чрез пойнтер - а"ptr".
//
//	Creates a pointer "ptr" that points to the memory address where the variable "var" is stored. 
//	This allows us to manipulate the value of "var" through a pointer - a "ptr".
//
//	Example:

//#include <iostream>
//
//int main() {
//    // Declare var
//    int var = 10;
//
//    // Create a pointer ptr that points to the memory address of var
//    int* ptr = &var;
//
//    // Output initial value of var
//    std::cout << "Value of var: " << var << std::endl;
//
//    // Output value of ptr
//    std::cout << "Value of ptr: " << *ptr << std::endl;
//
//    // Output memory address of var
//    std::cout << "Address of var: " << &var << std::endl;
//
//    // Output value of ptr
//    std::cout << "Address of ptr: " << ptr << std::endl;
//
//    // Change the value of ptr
//    *ptr = 20;
//
//    // Output value of var
//    std::cout << "New value of var: " << var << std::endl;
//
//    // Output value of ptr
//    std::cout << "New value of ptr: " << *ptr << std::endl;
